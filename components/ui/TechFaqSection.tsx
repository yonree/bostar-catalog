function formatAnswer(markdown: string): string {
  let html = markdown;
  // Bold → <strong>
  html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  // Inline code → <code>
  html = html.replace(/`([^`]+)`/g, '<code>$1</code>');
  // Table rendering
  html = html.replace(/\|(.+)\|/g, (line) => {
    const cells = line.split('|').filter((c) => c.trim());
    const isHeader = line.includes('---');
    if (isHeader) return '';
    const tag = line.startsWith('| ') && !line.includes('---') ? 'td' : 'td';
    return `<tr>${cells.map((c) => `<${tag}>${c.trim()}</${tag}>`).join('')}</tr>`;
  });
  // Wrap consecutive <tr>s in <table>
  html = html.replace(/(<tr>.*?<\/tr>\s*)+/g, (match) => {
    const rows = match.trim();
    return `<div class="my-4 overflow-x-auto"><table class="w-full text-left text-sm"><tbody>${rows}</tbody></table></div>`;
  });
  // Lists
  html = html.replace(/^\d+\.\s(.+)$/gm, '<li>$1</li>');
  html = html.replace(/((?:<li>.*?<\/li>\s*)+)/g, '<ol class="list-decimal pl-5 space-y-2 my-3">$1</ol>');
  // Paragraphs: split by double newline
  const blocks = html.split(/\n\n+/);
  return blocks
    .map((b) => {
      const trimmed = b.trim();
      if (!trimmed) return '';
      if (trimmed.startsWith('<div') || trimmed.startsWith('<ol') || trimmed.startsWith('<table')) return trimmed;
      return `<p class="my-2">${trimmed}</p>`;
    })
    .join('\n');
}
